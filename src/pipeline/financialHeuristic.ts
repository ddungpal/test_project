// 금융(재테크) 도메인 신호 감지 — 경량·순수·결정적 휴리스틱. commentSignals.ts 톤 미러.
//   ★ 이건 '표시·기본값'용일 뿐이다. 수동 추가 claim의 is_financial 최종값은 사용자 토글이 결정한다(UI는 step3).
//      여기 결과는 토글의 '초기값 제안'에만 쓰고, 자동판정만으로 금융여부를 확정하지 않는다.
//   형태소 분석 없이 키워드·패턴(부분일치) 기반. DB·LLM·시각 의존 없음 → 단위 테스트 가능.

// 재테크 도메인 토픽어 — 세금·금리·수익률·원금·제도·금융상품 등. NFC 정규화 후 부분일치(includes).
//   commentSignals STOP에서 '금융 토픽어는 절대 빼지 않는다'고 보존한 그 신호들과 동일 결.
const FINANCIAL_TERMS = [
  // 수익·이자·세금
  "세금", "절세", "연말정산", "소득공제", "세액공제", "비과세", "과세", "원천징수",
  "금리", "이자", "수익률", "수익", "원금", "복리", "단리", "배당", "분배금",
  // 상품·계좌
  "ETF", "etf", "펀드", "주식", "채권", "연금", "청약", "적금", "예금", "통장", "계좌",
  "대출", "전세", "월세", "보험", "신용", "카드", "포인트", "환율",
  // 투자·자산·제도
  "투자", "자산", "재테크", "저축", "노후", "은퇴", "물가", "인플레이션", "코스피", "나스닥",
  "비상금", "목돈", "시드", "복지", "지원금", "보조금", "혜택", "한도", "납입",
];

// 수치·금액 패턴 — %·금액 단위가 있으면 금융 신호로 본다(수익률·세율·금액 비교는 강검증 대상).
//   원/만원/억/퍼센트(% 기호 또는 '퍼센트' 표기)·연 N% 등.
const FINANCIAL_PATTERNS: RegExp[] = [
  /%/,
  /퍼센트|프로(?![그듀])/, // '프로그램/프로듀스' 오탐 회피용 negative lookahead
  /\d[\d,]*\s*(만원|억원|억|만\s*원|원\b|원을|원이|원의|원짜리)/,
  /연\s*\d/, // 연 5%, 연 3.5 등 이율 표기
  /\d+(\.\d+)?\s*만/, // 100만, 3.5만 등
];

/** 텍스트에 금융/세금/금리/수익률/원금/제도/% 등 재테크 신호가 있으면 true.
 *  순수·결정적(같은 입력 → 같은 출력). 표시·기본값용 — 최종 금융여부는 사용자 토글이 결정한다. */
export function detectFinancial(text: string): boolean {
  if (!text) return false;
  const t = text.normalize("NFC");
  // 키워드 부분일치(대소문자 구분 — ETF/etf 양쪽을 목록에 둠).
  for (const term of FINANCIAL_TERMS) {
    if (t.includes(term)) return true;
  }
  // 수치·금액·% 패턴.
  for (const re of FINANCIAL_PATTERNS) {
    if (re.test(t)) return true;
  }
  return false;
}
